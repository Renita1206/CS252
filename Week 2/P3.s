; ADD SUM OF N NATURAL NUMBERS AND STORE IN ANOTHER MEMORY LOCATION

.DATA
    SUM: .WORD 0

.TEXT 
    LDR R2, =SUM

    MOV R4, #1 ; COUNT REGISTER
    MOV R5, #0 ; INTERMEDIATE SUM

    LOOP:   ADD R5, R5, R4;
            ADD R4, R4, #1;
            CMP R4, #11 ; FOR N=10
            BNE LOOP
    STR R5, [R2]
    SWI 0X011
